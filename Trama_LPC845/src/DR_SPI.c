/**
	\fn  SPI_Init
	\brief Descripcion
 	\author ALEXIS VEGA
 	\date 30 sep. 2021
 	\param [in] parametros de entrada
 	\param [out] parametros de salida
	\return tipo y descripcion de retorno
*/
void SPI_Init(void)
{
	SYSAHBCLKCTRL0 |= (1 << SYS_CLK_CTRL_SPI0 );

	PRESETCTRL0 |= ( 1 << SPI0_RST_N );

	ISER0 |= ( 1 << ISE_SPI0);

}
